#include "libft.h"

int		ft_indexchr(const char *str, const char c)
{
	size_t		i;

	i = 0;
	while (str[i] && str[i] != c)
		i++;
	if (str[i] != '\0' || c == '\0')
		return (i);
	return (-1);
}
