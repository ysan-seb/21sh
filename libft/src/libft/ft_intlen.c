#include "libft.h"

int		ft_intlen(int n)
{
	int		len;
	double	pow;
	long	l;

	len = 0;
	pow = 1;
	if (n < 0)
		len++;
	l = (long)n;
	while (ABS(l) >= (pow *= 10))
		len++;
	return (len + 1);
}
